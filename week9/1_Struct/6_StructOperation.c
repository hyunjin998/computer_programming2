#include <stdio.h>

typedef struct point {
	int xpos;
	int ypos;
} Point;


int main() {
	Point pos1 = { 1, 2 };
	Point pos2;
	pos2 = pos1; // pos1의 멤버 대 pos2의 멤버간 복사가 진행됨

	printf("크기: %d\n", sizeof(pos1)); // 8, pos1의 전체 크기 반환
	printf("[%d, %d]\n", pos1.xpos, pos1.ypos); // [1, 2]
	printf("크기: %d\n", sizeof(pos2)); // 8, pos2의 전체 크기 반환
	printf("[%d, %d]\n", pos2.xpos, pos2.ypos); // [1, 2]
	return 0;
}